def display_rainbow(colors)
  colors = ["red", "orange", "yellow", "green", "blue", "indigo", "violet"]
  puts "red" + colors[0]
  puts "orange" + colors[1]
  puts "yellow" + colors[2]
  puts "green" + colors[3]
  puts "blue" + colors[4]
  puts "indigo" + colors[5]
  puts "violet" + colors[6]
end
